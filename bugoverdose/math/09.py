# 별 찍기 - 10

# 재귀
N = int(input()) # 3의 거듭제곱만 대입(3, 9, 27, ...)

def star(y, x, n, to_print):
    if (y//n)%3 == 1 and (x//n)%3 == 1:
        to_print += " "
    else:
        if n//3 == 0:
            to_print += "*"
        else:
            to_print = star(y, x, n//3, to_print) # 3의 거듭제곱 중 더 낮은 단계를 기준으로 판단
    return to_print

for y in range(N): # 각 줄의 각 칸에 대해 개별적으로 *인지 공백인지 판단
    to_print = ""
    for x in range(N):
        to_print = star(y, x, N, to_print)
    print(to_print) # 한줄씩 출력

